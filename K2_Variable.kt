fun main() {
    var x =10 // var is the real variable
    x=3
    println(x)

    val name = "Sanak" // To find the data type press (CTRL + Shift+ P)
    println(name)   //val is like constant

    // Lets learn some data type
    var score: Int = 98 // This is Intiger type data
    println(score)

    var parcentage: Double = 3.5 // This is Floting type data
    println(parcentage)

    var isRainnig: Boolean = false // This is Boolean
    println(isRainnig)

    var alphabet: Char = 'A' // This is Character
    println(alphabet)

    var text: String = " I am Anonymas"
    println(text)



}
