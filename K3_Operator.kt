fun main() {
    var i = 10
    var j = 3
    // '=' is a asgine operator
    println(i + j)  // Out put should be 13
    println(i - j)  // Out put should be 7
    println(i * j)  // Out put should be 30
    println(i.toFloat() / j)  // Out put should be 3.33 | To convert i (integer) to float we add .toFloat
    println(i % j)  // Out put should be 1; 10 /3 and 1 is left behind
    println(i > j) // '>' it is getter then operator
    println(i < j) // '<' it is lass then operator
    println(i >= j) // '>=' it is getter then equal to operator
    println(i <= j) // '<=' it is less then equal to operator
    println(i == j) // '==' it is the real equal to operator in coding
    println(i != j) // '!=' it is the not equal to operator


}

